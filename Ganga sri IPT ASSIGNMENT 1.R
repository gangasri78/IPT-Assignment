library(robotstxt)
library(rvest)
library(stringr)
library(dplyr)
url<-("https://www.flipkart.com/search?q=television&sid=ckf%2Cczl&as=on&as-show=on&otracker=AS_QueryStore_OrganicAutoSuggest_2_4_na_na_na&otracker1=AS_QueryStore_OrganicAutoSuggest_2_4_na_na_na&as-pos=2&as-type=RECENT&suggestionId=television%7CTelevisions&requestId=af5087e5-d8f4-4642-9a07-f7149087c958&as-searchtext=television")
path=paths_allowed(url)
web=read_html(url)
View(web)
names<-web%>%html_nodes(".KzDlHZ")%>%html_text()
View(names)
price<-web%>%html_nodes("._4b5DiR")%>%html_text()
View(price)
os<-web%>%html_nodes(".J\\+igdf:nth-child(1)")%>%html_text()
View(os)
launching_year<-web%>%html_nodes(".J\\+igdf:nth-child(3)")%>%html_text()
View(launching_year)
display<-web%>%html_nodes(".J\\+igdf:nth-child(2)")%>%html_text()
View(display)
tv_info=data.frame(names,price,os,launching_year,display)
View(tv_info)
write.csv(tv_info,"flipkart_tv.csv")
read.csv("flipkart_tv.csv")
